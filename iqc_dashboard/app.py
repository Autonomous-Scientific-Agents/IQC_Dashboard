def highlight_string_differences(str1: str, str2: str) -> Tuple[str, str]:
    """
    Highlight differences between two strings using HTML markup.
    Returns two strings with differences highlighted in red.

    Args:
        str1: First string (initial SMILES)
        str2: Second string (optimized SMILES)

    Returns:
        Tuple of (highlighted_str1, highlighted_str2) with HTML markup
    """
    if str1 == str2:
        return str1, str2

    # Use SequenceMatcher for accurate diff
    matcher = difflib.SequenceMatcher(None, str1, str2)

    highlighted1_parts = []
    highlighted2_parts = []

    for tag, i1, i2, j1, j2 in matcher.get_opcodes():
        if tag == "equal":
            # Same parts - no highlighting, but escape HTML
            highlighted1_parts.append(html.escape(str1[i1:i2]))
            highlighted2_parts.append(html.escape(str2[j1:j2]))
        elif tag == "delete":
            # Only in str1 - highlight in red, leave str2 empty for this segment
            highlighted1_parts.append(
                f'<mark style="background-color: #ffcccc;">{html.escape(str1[i1:i2])}</mark>'
            )
            # Don't add anything to highlighted2_parts for deleted segments
        elif tag == "insert":
            # Only in str2 - highlight in red, leave str1 empty for this segment
            highlighted2_parts.append(
                f'<mark style="background-color: #ffcccc;">{html.escape(str2[j1:j2])}</mark>'
            )
            # Don't add anything to highlighted1_parts for inserted segments
        elif tag == "replace":
            # Different in both - highlight both
            highlighted1_parts.append(
                f'<mark style="background-color: #ffcccc;">{html.escape(str1[i1:i2])}</mark>'
            )
            highlighted2_parts.append(
                f'<mark style="background-color: #ffcccc;">{html.escape(str2[j1:j2])}</mark>'
            )

    return "".join(highlighted1_parts), "".join(highlighted2_parts)


def normalize_vibrational_frequencies(frequencies) -> Optional[np.ndarray]:
    """
    Normalize vibrational frequencies into a numeric 1D numpy array.

    Args:
        frequencies: Frequency collection from the dataset.

    Returns:
        A 1D numpy array of finite float values, or None if the input cannot be parsed.
    """
    if frequencies is None:
        return None
    if isinstance(frequencies, float) and pd.isna(frequencies):
        return None

    if isinstance(frequencies, np.ndarray):
        freq_array = frequencies
    elif isinstance(frequencies, list):
        freq_array = np.array(frequencies)
    elif hasattr(frequencies, "__iter__") and not isinstance(frequencies, (str, bytes)):
        freq_array = np.array(list(frequencies))
    else:
        return None

    try:
        freq_array = np.asarray(freq_array, dtype=float).reshape(-1)
    except (TypeError, ValueError):
        return None

    freq_array = freq_array[np.isfinite(freq_array)]
    if len(freq_array) == 0:
        return None

    return freq_array


def normalize_spectrum_intensities(intensities, expected_length: int) -> Optional[np.ndarray]:
    """
    Normalize spectrum intensities into a numeric 1D numpy array.

    Args:
        intensities: Intensity collection from the dataset.
        expected_length: Required number of intensity values.

    Returns:
        A 1D numpy array of finite float values, or None if unavailable or mismatched.
    """
    if intensities is None:
        return None
    if isinstance(intensities, float) and pd.isna(intensities):
        return None

    if isinstance(intensities, np.ndarray):
        intensity_array = intensities
    elif isinstance(intensities, list):
        intensity_array = np.array(intensities)
    elif hasattr(intensities, "__iter__") and not isinstance(intensities, (str, bytes)):
        intensity_array = np.array(list(intensities))
    else:
        return None

    try:
        intensity_array = np.asarray(intensity_array, dtype=float).reshape(-1)
    except (TypeError, ValueError):
        return None

    if len(intensity_array) != expected_length or not np.all(np.isfinite(intensity_array)):
        return None

    return intensity_array


def _clean_unit(unit) -> Optional[str]:
    """Return a displayable unit string, or None when unavailable."""
    if unit is None:
        return None
    try:
        if pd.isna(unit):
            return None
    except (TypeError, ValueError):
        pass

    unit_text = str(unit).strip()
    if not unit_text:
        return None
    return unit_text


def _get_first_present(mapping, keys: Tuple[str, ...]):
    """Return the first non-null value from a dict-like object."""
    for key in keys:
        value = mapping.get(key, None)
        if value is None:
            continue
        try:
            if pd.isna(value):
                continue
        except (TypeError, ValueError):
            pass
        return value
    return None


def sync_indexed_selection(
    session_state,
    options: List[str],
    selection_key: str,
    index_key: Optional[str] = None,
) -> int:
    """Keep a selectbox value and optional selected index aligned."""
    if not options:
        if index_key is not None:
            session_state[index_key] = None
        return 0

    selected_value = session_state.get(selection_key)
    if selected_value in options:
        selected_index = options.index(selected_value)
    else:
        selected_index = session_state.get(index_key, 0) if index_key is not None else 0
        if selected_index is None:
            selected_index = 0
        selected_index = max(0, min(int(selected_index), len(options) - 1))
        session_state[selection_key] = options[selected_index]

    if index_key is not None:
        session_state[index_key] = selected_index

    return selected_index


def move_indexed_selection(
    session_state,
    options: List[str],
    selection_key: str,
    step: int,
    index_key: Optional[str] = None,
) -> int:
    """Move a selectbox-backed selection by step and persist both value and index."""
    if not options:
        if index_key is not None:
            session_state[index_key] = None
        return 0

    current_index = sync_indexed_selection(
        session_state,
        options,
        selection_key,
        index_key,
    )
    next_index = max(0, min(current_index + step, len(options) - 1))
    session_state[selection_key] = options[next_index]
    if index_key is not None:
        session_state[index_key] = next_index

    return next_index


def validate_energy_unit(energy_unit: str) -> str:
    """Validate and return a supported display energy unit."""
    if energy_unit not in ENERGY_UNITS:
        raise ValueError(f"Unsupported energy unit: {energy_unit}")
    return energy_unit


def energy_conversion_factor(energy_unit: str) -> float:
    """Return the multiplier for converting eV values to the selected unit."""
    validate_energy_unit(energy_unit)
    if energy_unit == ENERGY_UNIT_KCAL:
        return EV_TO_KCAL_MOL
    return 1.0


def convert_energy_value(value, energy_unit: str) -> Optional[float]:
    """Convert a scalar eV value to the selected display unit."""
    if value is None:
        return None
    try:
        if pd.isna(value):
            return None
    except (TypeError, ValueError):
        pass

    return float(value) * energy_conversion_factor(energy_unit)


def convert_energy_series(series: pd.Series, energy_unit: str) -> pd.Series:
    """Convert a pandas Series of eV values to the selected display unit."""
    return pd.to_numeric(series, errors="coerce") * energy_conversion_factor(energy_unit)


def format_energy_value(value, energy_unit: str, precision: int = 4) -> str:
    """Format a scalar eV value in the selected display unit."""
    converted = convert_energy_value(value, energy_unit)
    if converted is None:
        return "N/A"
    return f"{converted:.{precision}f} {energy_unit}"


def energy_metadata_label(column_name: str, energy_unit: str) -> str:
    """Convert an eV-backed metadata column name into a selected-unit display label."""
    validate_energy_unit(energy_unit)
    if column_name == "S_eV/K":
        return f"S ({energy_unit}/K)" if energy_unit == ENERGY_UNIT_KCAL else "S (eV/K)"
    if column_name.endswith("_eV"):
        return f"{column_name[:-3]} ({energy_unit})"
    return column_name


def is_energy_metadata_column(column_name: str) -> bool:
    """Return True for scalar metadata columns backed by eV values."""
    return column_name.endswith("_eV") or column_name == "S_eV/K"


def is_missing_scalar(value) -> bool:
    """Return True for scalar null values without treating arrays as ambiguous."""
    if value is None:
        return True
    if isinstance(value, (list, tuple, np.ndarray)):
        return False
    try:
        return bool(pd.isna(value))
    except (TypeError, ValueError):
        return False


def format_all_data_value(column_name: str, value, energy_unit: str) -> str:
    """Format one selected-molecule field for the All Data table."""
    if is_missing_scalar(value):
        return "N/A"
    if isinstance(value, np.ndarray):
        return str(value.tolist())
    if isinstance(value, (list, tuple)):
        return str(value)
    if is_energy_metadata_column(column_name):
        return format_energy_value(value, energy_unit, precision=6)
    return str(value)


def build_all_data_table(molecule_data: pd.Series, energy_unit: str) -> pd.DataFrame:
    """Build a complete selected-molecule row table for display."""
    rows = []
    for column_name, value in molecule_data.items():
        rows.append(
            {
                "Field": energy_metadata_label(column_name, energy_unit),
                "Value": format_all_data_value(column_name, value, energy_unit),
            }
        )

    return pd.DataFrame(rows, columns=["Field", "Value"])


def normalize_element_symbol(raw_symbol: str) -> Optional[str]:
    """Normalize an XYZ atom symbol for display and radius lookup."""
    match = re.match(r"([A-Za-z]+)", str(raw_symbol).strip())
    if not match:
        return None
    symbol = match.group(1)
    return symbol[:1].upper() + symbol[1:].lower()


def parse_xyz_coordinates(xyz_string: str) -> Optional[Tuple[List[str], np.ndarray]]:
    """Parse XYZ text into element symbols and coordinates."""
    if not xyz_string or is_missing_scalar(xyz_string):
        return None

    lines = [line.strip() for line in str(xyz_string).splitlines() if line.strip()]
    if not lines:
        return None

    atom_lines = lines
    try:
        atom_count = int(lines[0])
        atom_lines = lines[2 : 2 + atom_count]
    except (ValueError, IndexError):
        pass

    elements = []
    coordinates = []
    for line in atom_lines:
        parts = line.split()
        if len(parts) < 4:
            continue

        element = normalize_element_symbol(parts[0])
        if element is None:
            continue

        try:
            coordinate = [float(parts[1]), float(parts[2]), float(parts[3])]
        except ValueError:
            continue

        elements.append(element)
        coordinates.append(coordinate)

    if not elements:
        return None

    return elements, np.array(coordinates, dtype=float)


def atom_label(elements: List[str], atom_index: int) -> str:
    """Return a compact atom label using element plus one-based atom index."""
    return f"{elements[atom_index]}{atom_index + 1}"


def align_coordinates(
    reference_coords: np.ndarray,
    mobile_coords: np.ndarray,
    fit_indices: List[int],
) -> np.ndarray:
    """Align mobile coordinates onto reference coordinates using Kabsch fitting."""
    if len(fit_indices) == 0:
        fit_indices = list(range(len(reference_coords)))

    reference_fit = reference_coords[fit_indices]
    mobile_fit = mobile_coords[fit_indices]
    reference_centroid = reference_fit.mean(axis=0)
    mobile_centroid = mobile_fit.mean(axis=0)

    if len(fit_indices) < 2:
        return mobile_coords - mobile_centroid + reference_centroid

    reference_centered = reference_fit - reference_centroid
    mobile_centered = mobile_fit - mobile_centroid
    covariance = mobile_centered.T @ reference_centered
    left, _, right_t = np.linalg.svd(covariance)
    rotation = right_t.T @ left.T

    if np.linalg.det(rotation) < 0:
        right_t[-1, :] *= -1
        rotation = right_t.T @ left.T

    return (mobile_coords - mobile_centroid) @ rotation + reference_centroid


def calculate_distance(coords: np.ndarray, atom_i: int, atom_j: int) -> float:
    """Calculate an interatomic distance."""
    return float(np.linalg.norm(coords[atom_i] - coords[atom_j]))


def calculate_angle(coords: np.ndarray, atom_i: int, atom_j: int, atom_k: int) -> float:
    """Calculate an angle in degrees for i-j-k."""
    vec_a = coords[atom_i] - coords[atom_j]
    vec_b = coords[atom_k] - coords[atom_j]
    norm_product = np.linalg.norm(vec_a) * np.linalg.norm(vec_b)
    if norm_product == 0:
        return float("nan")

    cosine = np.clip(np.dot(vec_a, vec_b) / norm_product, -1.0, 1.0)
    return float(np.degrees(np.arccos(cosine)))


def calculate_dihedral(
    coords: np.ndarray,
    atom_i: int,
    atom_j: int,
    atom_k: int,
    atom_l: int,
) -> float:
    """Calculate a signed dihedral angle in degrees for i-j-k-l."""
    p0 = coords[atom_i]
    p1 = coords[atom_j]
    p2 = coords[atom_k]
    p3 = coords[atom_l]

    b0 = -(p1 - p0)
    b1 = p2 - p1
    b2 = p3 - p2
    b1_norm = np.linalg.norm(b1)
    if b1_norm == 0:
        return float("nan")

    b1 = b1 / b1_norm
    v = b0 - np.dot(b0, b1) * b1
    w = b2 - np.dot(b2, b1) * b1
    v_norm = np.linalg.norm(v)
    w_norm = np.linalg.norm(w)
    if v_norm == 0 or w_norm == 0:
        return float("nan")

    x = np.dot(v, w)
    y = np.dot(np.cross(b1, v), w)
    return float(np.degrees(np.arctan2(y, x)))


def normalize_angle_delta(delta: float) -> float:
    """Normalize an angle delta to the [-180, 180] interval."""
    return float(((delta + 180.0) % 360.0) - 180.0)


def infer_bonds(elements: List[str], coords: np.ndarray) -> set[Tuple[int, int]]:
    """Infer covalent bonds from interatomic distances and covalent radii."""
    bonds = set()
    for atom_i in range(len(elements)):
        radius_i = COVALENT_RADII_ANGSTROM.get(elements[atom_i], 0.77)
        for atom_j in range(atom_i + 1, len(elements)):
            radius_j = COVALENT_RADII_ANGSTROM.get(elements[atom_j], 0.77)
            distance = calculate_distance(coords, atom_i, atom_j)
            if 0.35 <= distance <= 1.25 * (radius_i + radius_j):
                bonds.add((atom_i, atom_j))
    return bonds


def build_bond_adjacency(bonds: set[Tuple[int, int]], atom_count: int) -> List[set[int]]:
    """Build adjacency sets from inferred bonds."""
    adjacency = [set() for _ in range(atom_count)]
    for atom_i, atom_j in bonds:
        adjacency[atom_i].add(atom_j)
        adjacency[atom_j].add(atom_i)
    return adjacency


def find_angle_tuples(adjacency: List[set[int]]) -> List[Tuple[int, int, int]]:
    """Find unique bonded angle tuples i-j-k."""
    angles = []
    for center_atom, neighbors in enumerate(adjacency):
        sorted_neighbors = sorted(neighbors)
        for left_index, atom_i in enumerate(sorted_neighbors):
            for atom_k in sorted_neighbors[left_index + 1 :]:
                angles.append((atom_i, center_atom, atom_k))
    return angles


def find_dihedral_tuples(
    bonds: set[Tuple[int, int]],
    adjacency: List[set[int]],
) -> List[Tuple[int, int, int, int]]:
    """Find unique bonded dihedral tuples i-j-k-l."""
    dihedrals = []
    seen = set()
    for atom_j, atom_k in sorted(bonds):
        for atom_i in sorted(adjacency[atom_j] - {atom_k}):
            for atom_l in sorted(adjacency[atom_k] - {atom_j}):
                if atom_i == atom_l:
                    continue
                forward = (atom_i, atom_j, atom_k, atom_l)
                reverse = (atom_l, atom_k, atom_j, atom_i)
                canonical = min(forward, reverse)
                if canonical in seen:
                    continue
                seen.add(canonical)
                dihedrals.append(forward)
    return dihedrals


def rank_geometry_changes(rows: List[dict], delta_column: str, limit: int) -> pd.DataFrame:
    """Sort geometry changes by absolute delta and return the top rows."""
    if not rows:
        return pd.DataFrame()

    ranked = pd.DataFrame(rows)
    ranked["_abs_delta"] = ranked[delta_column].abs()
    ranked = ranked.sort_values("_abs_delta", ascending=False).drop(columns=["_abs_delta"])
    return ranked.head(limit).reset_index(drop=True)


def build_geometry_optimization_summary(
    molecule_data: pd.Series,
    energy_unit: str,
    limit: int = 5,
) -> Optional[dict]:
    """Build geometry optimization summary data from initial and optimized XYZ structures."""
    initial_parsed = parse_xyz_coordinates(molecule_data.get("initial_xyz", None))
    optimized_parsed = parse_xyz_coordinates(molecule_data.get("opt_xyz", None))
    if initial_parsed is None or optimized_parsed is None:
        return None

    elements_initial, initial_coords = initial_parsed
    elements_optimized, optimized_coords = optimized_parsed
    if elements_initial != elements_optimized or len(initial_coords) != len(optimized_coords):
        return None

    heavy_indices = [
        index for index, element in enumerate(elements_initial) if element.upper() != "H"
    ]
    fit_indices = heavy_indices or list(range(len(elements_initial)))
    aligned_optimized_coords = align_coordinates(initial_coords, optimized_coords, fit_indices)
    displacements = np.linalg.norm(aligned_optimized_coords - initial_coords, axis=1)
    heavy_rmsd = float(np.sqrt(np.mean(displacements[fit_indices] ** 2)))
    max_displacement = float(np.max(displacements)) if len(displacements) else 0.0

    initial_bonds = infer_bonds(elements_initial, initial_coords)
    optimized_bonds = infer_bonds(elements_initial, optimized_coords)
    bonds = initial_bonds | optimized_bonds
    adjacency = build_bond_adjacency(bonds, len(elements_initial))

    bond_rows = []
    for atom_i, atom_j in sorted(bonds):
        initial_distance = calculate_distance(initial_coords, atom_i, atom_j)
        optimized_distance = calculate_distance(optimized_coords, atom_i, atom_j)
        bond_rows.append(
            {
                "Bond": f"{atom_label(elements_initial, atom_i)}-{atom_label(elements_initial, atom_j)}",
                "Initial (Å)": initial_distance,
                "Optimized (Å)": optimized_distance,
                "Δ (Å)": optimized_distance - initial_distance,
            }
        )

    angle_rows = []
    for atom_i, atom_j, atom_k in find_angle_tuples(adjacency):
        initial_angle = calculate_angle(initial_coords, atom_i, atom_j, atom_k)
        optimized_angle = calculate_angle(optimized_coords, atom_i, atom_j, atom_k)
        if not np.isfinite(initial_angle) or not np.isfinite(optimized_angle):
            continue
        angle_rows.append(
            {
                "Angle": (
                    f"{atom_label(elements_initial, atom_i)}-"
                    f"{atom_label(elements_initial, atom_j)}-"
                    f"{atom_label(elements_initial, atom_k)}"
                ),
                "Initial (°)": initial_angle,
                "Optimized (°)": optimized_angle,
                "Δ (°)": optimized_angle - initial_angle,
            }
        )

    dihedral_rows = []
    for atom_i, atom_j, atom_k, atom_l in find_dihedral_tuples(bonds, adjacency):
        initial_dihedral = calculate_dihedral(initial_coords, atom_i, atom_j, atom_k, atom_l)
        optimized_dihedral = calculate_dihedral(optimized_coords, atom_i, atom_j, atom_k, atom_l)
        if not np.isfinite(initial_dihedral) or not np.isfinite(optimized_dihedral):
            continue
        dihedral_rows.append(
            {
                "Dihedral": (
                    f"{atom_label(elements_initial, atom_i)}-"
                    f"{atom_label(elements_initial, atom_j)}-"
                    f"{atom_label(elements_initial, atom_k)}-"
                    f"{atom_label(elements_initial, atom_l)}"
                ),
                "Initial (°)": initial_dihedral,
                "Optimized (°)": optimized_dihedral,
                "Δ (°)": normalize_angle_delta(optimized_dihedral - initial_dihedral),
            }
        )

    initial_energy = molecule_data.get("initial_energy_eV", None)
    optimized_energy = molecule_data.get("opt_energy_eV", None)
    energy_change = None
    if not is_missing_scalar(initial_energy) and not is_missing_scalar(optimized_energy):
        energy_change = convert_energy_value(
            float(optimized_energy) - float(initial_energy),
            energy_unit,
        )

    return {
        "energy_change": energy_change,
        "heavy_atom_rmsd": heavy_rmsd,
        "max_atom_displacement": max_displacement,
        "bond_changes": rank_geometry_changes(bond_rows, "Δ (Å)", limit),
        "angle_changes": rank_geometry_changes(angle_rows, "Δ (°)", limit),
        "dihedral_changes": rank_geometry_changes(dihedral_rows, "Δ (°)", limit),
    }


def create_ir_spectrum_plot(
    frequencies,
    intensities,
    title: str,
    frequency_units=None,
    intensity_units=None,
) -> Optional[go.Figure]:
    """
    Create an IR spectrum curve from paired spectrum frequency and intensity arrays.

    Args:
        frequencies: Spectrum frequency collection from the dataset.
        intensities: Spectrum intensity collection from the dataset.
        title: Plot title.
        frequency_units: Optional frequency unit label.
        intensity_units: Optional intensity unit label.

    Returns:
        Plotly figure, or None if paired spectrum data is unavailable.
    """
    freq_array = normalize_vibrational_frequencies(frequencies)
    if freq_array is None:
        return None

    intensity_array = normalize_spectrum_intensities(intensities, len(freq_array))
    if intensity_array is None:
        return None

    frequency_unit = _clean_unit(frequency_units) or "cm^-1"
    intensity_unit = _clean_unit(intensity_units)
    intensity_label = "IR Intensity"
    if intensity_unit:
        intensity_label = f"{intensity_label} ({intensity_unit})"

    fig = go.Figure()
    fig.add_trace(
        go.Scatter(
            x=freq_array,
            y=intensity_array,
            mode="lines",
            line=dict(color="#1f77b4", width=2),
            name="IR Spectrum",
            hovertemplate=(
                f"Frequency: %{{x:.2f}} {frequency_unit}<br>"
                "Intensity: %{y:.4g}<extra></extra>"
            ),
        )
    )
    fig.update_layout(
        title=title,
        xaxis_title=f"Frequency ({frequency_unit})",
        yaxis_title=intensity_label,
        height=420,
    )

    return fig


def build_vibrational_frequency_table(frequencies, intensities=None) -> pd.DataFrame:
    """
    Build a table of vibrational modes for display.

    Args:
        frequencies: Frequency collection from the dataset.
        intensities: Optional spectrum intensity collection.

    Returns:
        DataFrame with mode number, frequency, and mode type.
    """
    freq_array = normalize_vibrational_frequencies(frequencies)
    columns = ["Mode", "Frequency (cm^-1)", "Type"]
    if freq_array is None:
        return pd.DataFrame(columns=columns)

    table = pd.DataFrame(
        {
            "Mode": np.arange(1, len(freq_array) + 1),
            "Frequency (cm^-1)": freq_array,
            "Type": np.where(freq_array < 0, "Imaginary", "Real"),
        }
    )

    intensity_array = normalize_spectrum_intensities(intensities, len(freq_array))
    if intensity_array is not None:
        table["Intensity"] = intensity_array

    return table


def create_vibrational_stick_plot(
    frequencies,
    title: str,
    intensities=None,
    frequency_units=None,
    intensity_units=None,
) -> Optional[go.Figure]:
    """
    Create a stick plot for vibrational frequencies.

    Args:
        frequencies: Frequency collection from the dataset.
        title: Plot title.
        intensities: Optional spectrum intensity collection.
        frequency_units: Optional frequency unit label.
        intensity_units: Optional intensity unit label.

    Returns:
        Plotly figure, or None if no valid frequencies are available.
    """
    freq_array = normalize_vibrational_frequencies(frequencies)
    if freq_array is None:
        return None

    intensity_array = normalize_spectrum_intensities(intensities, len(freq_array))
    uses_intensities = intensity_array is not None
    plot_heights = intensity_array if uses_intensities else np.ones(len(freq_array))
    frequency_unit = _clean_unit(frequency_units) or "cm^-1"
    intensity_unit = _clean_unit(intensity_units)

    def _segment_trace(
        values: np.ndarray,
        heights: np.ndarray,
        name: str,
        color: str,
    ) -> Optional[go.Scatter]:
        if len(values) == 0:
            return None

        x_coords = []
        y_coords = []
        for value, height in zip(values, heights):
            x_coords.extend([value, value, None])
            y_coords.extend([0, height, None])

        return go.Scatter(
            x=x_coords,
            y=y_coords,
            mode="lines",
            line=dict(color=color, width=2),
            name=name,
            hoverinfo="skip",
        )

    fig = go.Figure()

    imaginary_mask = freq_array < 0
    imag_trace = _segment_trace(
        freq_array[imaginary_mask],
        plot_heights[imaginary_mask],
        "Imaginary Modes",
        "#d62728",
    )
    real_trace = _segment_trace(
        freq_array[~imaginary_mask],
        plot_heights[~imaginary_mask],
        "Real Modes",
        "#1f77b4",
    )

    for trace in (imag_trace, real_trace):
        if trace is not None:
            fig.add_trace(trace)

    mode_types = np.where(imaginary_mask, "Imaginary", "Real")
    customdata = [
        [mode_type, intensity]
        for mode_type, intensity in zip(mode_types, plot_heights)
    ]
    yaxis_title = "Relative Intensity"
    if uses_intensities:
        yaxis_title = "IR Intensity"
        if intensity_unit:
            yaxis_title = f"{yaxis_title} ({intensity_unit})"

    fig.add_trace(
        go.Scatter(
            x=freq_array,
            y=plot_heights,
            mode="markers",
            marker=dict(
                size=8,
                color=np.where(imaginary_mask, "#d62728", "#1f77b4"),
            ),
            customdata=customdata,
            name="Modes",
            hovertemplate=(
                f"Frequency: %{{x:.2f}} {frequency_unit}<br>"
                "Intensity: %{customdata[1]:.4g}<br>"
                "Type: %{customdata[0]}<extra></extra>"
            ),
            showlegend=False,
        )
    )

    y_max = float(np.max(plot_heights)) if len(plot_heights) else 1.0
    if y_max <= 0:
        y_max = 1.0

    fig.update_layout(
        title=title,
        xaxis_title=f"Frequency ({frequency_unit})",
        yaxis_title=yaxis_title,
        yaxis=dict(range=[0, y_max * 1.1], showticklabels=uses_intensities),
        height=420,
    )

    return fig


def create_molecule_spectrum_plot(molecule_data, title: str) -> Optional[go.Figure]:
    """
    Create the best available IR spectrum plot for a molecule row.

    Uses paired spectrum_frequencies/spectrum_intensities first, then falls back to
    vibrational mode frequencies when only mode-level data is present.
    """
    spectrum_fig = create_ir_spectrum_plot(
        molecule_data.get("spectrum_frequencies", None),
        molecule_data.get("spectrum_intensities", None),
        title,
        molecule_data.get("spectrum_frequencies_units", None),
        molecule_data.get("spectrum_intensities_units", None),
    )
    if spectrum_fig is not None:
        return spectrum_fig

    frequencies = _get_first_present(
        molecule_data,
        ("vibrational_frequencies_cm^-1", "frequencies_cm^-1"),
    )
    return create_vibrational_stick_plot(
        frequencies,
        title,
        molecule_data.get("spectrum_intensities", None),
        "cm^-1",
        molecule_data.get("spectrum_intensities_units", None),
    )


def add_atom_labels(view, xyz_string: str) -> None:
    """Add element-index labels to a py3Dmol view."""
    parsed_xyz = parse_xyz_coordinates(xyz_string)
    if parsed_xyz is None:
        return

    elements, coords = parsed_xyz
    for atom_index, (element, coord) in enumerate(zip(elements, coords), start=1):
        view.addLabel(
            f"{element}{atom_index}",
            {
                "position": {
                    "x": float(coord[0]),
                    "y": float(coord[1]),
                    "z": float(coord[2]),
                },
                "fontColor": "black",
                "backgroundColor": "white",
                "fontSize": 10,
                "showBackground": True,
            },
        )


def set_molecule_style(view, style: str) -> None:
    """Apply a named molecular visualization style to a py3Dmol view."""
    if style == "stick":
        view.setStyle({"stick": {}})
    elif style == "ball_and_stick":
        view.setStyle({"stick": {"radius": 0.15}, "sphere": {"scale": 0.3}})
    elif style == "sphere":
        view.setStyle({"sphere": {"radius": 0.5}})
    elif style == "wireframe":
        view.setStyle({"line": {"linewidth": 2}})
    elif style == "cartoon":
        view.setStyle({"cartoon": {}})
    else:
        view.setStyle({"stick": {}, "sphere": {"radius": 0.3}})


def render_molecule(
    xyz_string: str,
    style: str = "stick",
    label: str = "",
    show_labels: bool = False,
) -> None:
    """
    Render a molecule in 3D using stmol.

    Args:
        xyz_string: XYZ format string of the molecule
        style: Visualization style ('stick', 'ball_and_stick', 'sphere', 'wireframe', etc.)
        label: Optional label to display
        show_labels: Whether to display atom labels
    """
    # Check if required libraries are available
    if not STMOL_AVAILABLE:
        st.error("stmol is not installed or cannot be imported.")
        st.error(f"Error details: {IMPORT_ERROR_MSG}")
        st.error("**Solution:** Install missing dependencies:")
        st.code("pip install ipython-genutils ipywidgets stmol", language="bash")
        return

    if not PY3DMOL_AVAILABLE:
        st.error("py3Dmol is not installed or cannot be imported.")
        st.error(f"Error details: {IMPORT_ERROR_MSG}")
        st.error("**Solution:** Install missing dependencies:")
        st.code("pip install py3Dmol", language="bash")
        return

    if not xyz_string or pd.isna(xyz_string):
        st.warning(f"No XYZ data available for {label}")
        return

    try:
        # Import here to ensure they're available
        import stmol
        import py3Dmol

        # Create py3Dmol viewer
        view = py3Dmol.view(width=400, height=400)
        view.addModel(xyz_string, "xyz")

        set_molecule_style(view, style)
        if show_labels:
            add_atom_labels(view, xyz_string)

        view.zoomTo()
        view.render()

        # Display using stmol
        stmol.showmol(view, height=400, width=400)

    except ImportError as e:
        st.error(f"Import error when rendering molecule: {e}")
        st.error("Please ensure both stmol and py3Dmol are installed in your environment.")
    except Exception as e:
        st.error(f"Error rendering molecule {label}: {e}")


